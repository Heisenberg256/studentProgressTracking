        barChartData.setValue(t2total,"Score","TE-SEM-II");
        barChartData.setValue(b1total,"Score","BE-SEM-I");
        barChartData.setValue(b2total,"Score","BE-SEM-II");

        JFreeChart barChart=ChartFactory.createBarChart("PROGRESS","SEMESTER","MARKS",barChartData,PlotOrientation.VERTICAL,false,true,false);
