    private void sendmailbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendmailbtnActionPerformed
        
        try{
            MongoClient client = new MongoClient("localhost", 27017);
            DB db = client.getDB("StudentProgressTracking");
            DBCollection student = db.getCollection("student");
            DBCollection mentor = db.getCollection("mentor");
            
            long temp1=(long)table.getValueAt(menteestable.getSelectedRow(),menteestable.getSelectedColumn());
            DBCursor cur = student.find(new BasicDBObject("_id", temp1));

            String date = ((JTextField)mentormeetdate.getDateEditor().getUiComponent()).getText();

            for(DBObject obj:cur){
                email = (String)obj.get("EmailID");
            }
            Email obj = new Email();
            obj.mail1(email,date);
            mentor.update(new BasicDBObject("Email_ID", currentUser), new BasicDBObject("$set", new BasicDBObject("MentorMeetDate", date)));
            
        }
        catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(this, "Please select a RollNo", "Oops", JOptionPane.ERROR_MESSAGE);
        }
        catch(ClassCastException e){
            JOptionPane.showMessageDialog(this, "Please select a RollNo", "Oops", JOptionPane.ERROR_MESSAGE);
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_sendmailbtnActionPerformed

