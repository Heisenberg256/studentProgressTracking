    private void savestudentdetailsbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savestudentdetailsbtnActionPerformed
        
        try{
            flag = true;
            stname = studentname.getText();
            bloodgrp = studentbldgroup.getText();
            email = studentemail.getText();
            area = studentarea.getText();
            city = studentcity.getText();
            rollno = Long.parseLong(studentrollno.getText());
            //rollno = Integer.parseInt(studentrollno.getText());
            mobileno = Long.parseLong(studentmobile.getText());
            pincode = Long.parseLong(studentpincode.getText());
            department = (String)departmentSelectComboBox.getSelectedItem();
            division = (String)divisionComboBox.getSelectedItem();
            year = (String)yearComboBox.getSelectedItem();
            confirmpassword = (String)studentconfirmpassw.getText();
            password = (String)studentpassword.getText();

            if(!password.equals(confirmpassword)){
                JOptionPane.showMessageDialog(this,"ReEnter the password","Password does not match",JOptionPane.ERROR_MESSAGE);
            }
            
            int length = studentmobile.getText().length();
            if(length > 10 || length < 10)
                throw new NumberFormatException();
            
            length = studentpincode.getText().length();
            if(length > 6 || length < 6)
                throw new NumberFormatException();
            
            length = studentpincode.getText().length();
            if(length > 6 || length < 6)
                throw new NumberFormatException();
            
            if(!email.matches(".*@.*\\.com")){
                JOptionPane.showMessageDialog(this, "Enter vaild email address","Oops",JOptionPane.ERROR_MESSAGE);
                flag = false;
            }
            
            date = ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
            if(date.equals(""))
                    throw new NumberFormatException();
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Please enter valid data in all fields", "Try Again", JOptionPane.ERROR_MESSAGE);
            flag = false;
        }
        if(flag == true)
            jProgressBar1.setValue(50);
    }//GEN-LAST:event_savestudentdetailsbtnActionPerformed

