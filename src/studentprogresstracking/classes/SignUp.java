                BasicDBObject address = new BasicDBObject("Area",area).append("City",city).append("Pincode",pincode);
                doc.append("Address",address).append("FatherName",fthname).append("MotherName",mthname);
                doc.append("FatherMobileNo", fthmobile).append("MotherMobileNo",mthmobile);

                BasicDBObject paddress = new BasicDBObject("Area",parentarea).append("City",parentcity).append("Pincode",parentpin);
                doc.append("ParentAddress",paddress);
                
                signupcount = student.count();
                signupcount++;
                
                long mentorId = signupcount%10;
                if(mentorId != 0)
                    doc.append("Mentor_ID",mentorId);
                else{
                    mentorId = (long)10;
                    doc.append("Mentor_ID",mentorId);
                }
                
                student.insert(doc);
                
                mentor.update(new BasicDBObject("Mentor_ID", mentorId), new BasicDBObject("$push", new BasicDBObject("Student_ID", rollno)));
                academic.insert(new BasicDBObject("Student_ID", rollno));
                
